# Вариант B.
# Перебираем элементы списка и добавляем их в словарь, где ключ == элемент, а значение == количеству таких элементов
# в исходном списке. Переберем все эелементы словаря, и те, значение которых равно 1, добавим в новый список.

my_list_1 = [2, 2, 5, 12, 8, 2, 12]
my_list_2 = []
my_dict = {}
for elem in my_list_1:
    my_dict[elem] = my_dict.get(elem, 0) + 1

for elem in my_dict:
    if my_dict[elem] == 1:
        my_list_2.append(elem)
