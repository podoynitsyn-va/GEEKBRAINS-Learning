# 3: Дан список заполненный произвольными целыми числами.
# Получите новый список, элементами которого будут только уникальные элементы исходного.
#    Примечание. Списки создайте вручную, например так:
# my_list_1 = [2, 2, 5, 12, 8, 2, 12]
# В этом случае ответ будет:
# [5, 8]
#-----------------------------------------------------------------------------------------------------------------------
# Вариант А.
# Создадим множетво на основе данного списка (чтобы сократить количество переборов). Переберем это множество и, с
# помощью метода count, определим количество таких элементов в исходном списке. Если элемент только один, добавляем его
# в новый список

print('===============================================')

my_list_1 = [2, 2, 5, 12, 8, 2, 12]
my_list_2 = []
my_set = set(my_list_1)
for elem in my_set:
    if my_list_1.count(elem) == 1:
        my_list_2.append(elem)
print(my_list_2)

print('-----------------------------------------------')
